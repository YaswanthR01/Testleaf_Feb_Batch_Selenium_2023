public class Swapping {
	public static void main(String[] args) {
		int a=20;
		int b=18;
		//to change the values do simple math
		a=a+b;
		//here the value is changed
		b=a-b;
		a=a-b;
		System.out.println("The swapped a is "+ a);
		System.out.println("The swapped b is "+ b );
	}

}
