package week3.day5.assignments;

public class Tables {
	public static void main(String[] args) {
		//to create a variable for 8 table
		int tab=8;
		//iterate the values from 1 to 10
		for (int i = 1; i <=10; i++) {
			//here we print the values and tab is our tables 
			System.out.println(tab+ "*"+i+"="+tab*i);
		}
	}

}
